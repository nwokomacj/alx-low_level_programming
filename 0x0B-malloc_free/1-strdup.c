#include <stdlib.h>
#include "main.h"

/**
 * *_strdup -returns a new string to the pointer
 * @str: string to duplicate
 *
 * Return: returns pointer to the duplicate string (Success), NULL (Error)
 */
char *_strdup(char *str)
{
	char *dup;
	unsigned int i, len;

	i = 0;
	len = 0;

	if (str == NULL)
		return (NULL);

	while (str[len])
		len++;

	dup = malloc(sizeof(char) + (len + 1));

	if (dup == NULL)
		return (NULL);

	while ((dup[i] = str[i]) != '\0')
		i++;

	return (dup);
}
